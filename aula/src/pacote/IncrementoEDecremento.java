package pacote;

public class IncrementoEDecremento {
	public static void main(String[] args) {

		int varInt = 1;
		int varInt2 = 4;

		
		System.out.println(varInt++);
		System.out.println(varInt);
		System.out.println(++varInt);

		System.out.println("");

		System.out.println(varInt2--);
		System.out.println(varInt2);
		System.out.println(--varInt2);
		
		
		

	}
}
