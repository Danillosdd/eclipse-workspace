package pacote;


public class Variaveis {
	
	public static void main(String[] args) {	
	
	String nome = "Danillo Araujo";
	String endereco = "Rua Três Marias";
	
	byte idade = 28;
	
	char estadoCivil = 'C';
	
	float salario = 2300f;
	
	System.out.println("Nome(String): " + nome);
	System.out.println("Endereço(String) : " + endereco);
	System.out.printf("Idade(byte): %d, %d%n", idade, idade);
	System.out.printf("Estado Civil(char): %c%n", estadoCivil);
	System.out.printf("Salário(float): %.2f%n", salario);
	
	
	
	} 

}
