package pacote;

public class Variaveis2 {

	public static void main(String[] args) {
		
		boolean a = false;
		Boolean b = true;
		
		double valor = 1;
		
		int numero = 1;
		Integer outroNumero = 2;

		long numeral = 3;
		Long outroNumeral = 4l;
		
		
		System.out.println(a);
		System.out.println(b);
		System.out.printf("Teste: %.2f%n",valor);
		System.out.println(numero);
		System.out.println(outroNumero);
		System.out.println(numeral);
		System.out.println(outroNumeral);
	}
	
}
